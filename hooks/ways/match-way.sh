#!/bin/bash
# Shared matching logic for ways — sourced by check-prompt.sh and check-task-pre.sh
#
# Usage:
#   source "${WAYS_DIR}/match-way.sh"
#   detect_semantic_engine
#   match_way_prompt "$prompt" "$pattern" "$description" "$vocabulary" "$threshold"
#     → returns 0 (match) or 1 (no match)

WAYS_DIR="${WAYS_DIR:-${HOME}/.claude/hooks/ways}"

# Find way files in a directory. A way file is any .md file with YAML frontmatter
# (starts with ---) in a way directory. Excludes *.check.md files.
# Outputs null-terminated paths suitable for while read -d ''.
find_way_files() {
  local dir="$1"
  find -L "$dir" -name "*.md" ! -name "*.check.md" -print0 2>/dev/null | while IFS= read -r -d '' f; do
    head -1 "$f" 2>/dev/null | grep -q '^---$' && printf '%s\0' "$f"
  done
}

# Extract way ID from a way file path relative to a ways directory.
# e.g., /home/user/.claude/hooks/ways/softwaredev/code/quality/quality.md → softwaredev/code/quality
way_id_from_path() {
  local filepath="$1" basedir="$2"
  local rel="${filepath#$basedir/}"
  echo "${rel%/*}"
}

# Check `when:` preconditions — deterministic gate before any matching.
# Returns 0 if all preconditions are met (or no when: block), 1 if any fail.
# Args: $1=frontmatter (raw text)
# Requires: PROJECT_DIR to be set by the calling scanner
check_when_preconditions() {
  local frontmatter="$1"

  # Extract when: block fields (indented under when:)
  local when_project
  when_project=$(echo "$frontmatter" | awk '/^when:/{found=1;next} found && /^  project:/{gsub(/^  project: */,"");print;exit} found && /^[^ ]/{exit}')

  local when_file_exists
  when_file_exists=$(echo "$frontmatter" | awk '/^when:/{found=1;next} found && /^  file_exists:/{gsub(/^  file_exists: */,"");print;exit} found && /^[^ ]/{exit}')

  # No when: block → no gate → allow
  [[ -z "$when_project" && -z "$when_file_exists" ]] && return 0

  # when.project: check if current project dir matches
  if [[ -n "$when_project" ]]; then
    local expanded_project="${when_project/#\~/$HOME}"
    local resolved_project
    resolved_project=$(cd "$expanded_project" 2>/dev/null && pwd -P || echo "$expanded_project")
    local resolved_current
    resolved_current=$(cd "${PROJECT_DIR:-.}" 2>/dev/null && pwd -P || echo "${PROJECT_DIR:-.}")

    [[ "$resolved_current" != "$resolved_project" ]] && return 1
  fi

  # when.file_exists: check if file exists relative to PROJECT_DIR
  if [[ -n "$when_file_exists" ]]; then
    local resolved_dir
    resolved_dir=$(cd "${PROJECT_DIR:-.}" 2>/dev/null && pwd -P || echo "${PROJECT_DIR:-.}")
    [[ ! -e "${resolved_dir}/${when_file_exists}" ]] && return 1
  fi

  return 0
}

# Detect semantic matcher: ways binary → legacy fallback → none
# Respects "semantic_engine" in ways.json: "auto" (default), "embedding", "bm25"
# Sets: SEMANTIC_ENGINE, WAYS_BIN, WAY_EMBED_BIN, MODEL_PATH, CORPUS_PATH, EMBED_CACHE, BM25_CACHE
detect_semantic_engine() {
  local xdg_cache="${XDG_CACHE_HOME:-$HOME/.cache}/claude-ways/user"
  WAYS_BIN="${HOME}/.claude/bin/ways"
  WAY_EMBED_BIN=""
  MODEL_PATH="${xdg_cache}/minilm-l6-v2.gguf"
  CORPUS_PATH=""
  EMBED_CACHE=""
  BM25_CACHE=""

  # Corpus lives in XDG cache (generated by ways corpus / make setup)
  local corpus_file="${xdg_cache}/ways-corpus.jsonl"
  [[ -f "$corpus_file" ]] && CORPUS_PATH="$corpus_file"

  # way-embed: check XDG cache first (downloaded), then ~/.claude/bin
  if [[ -x "${xdg_cache}/way-embed" ]]; then
    WAY_EMBED_BIN="${xdg_cache}/way-embed"
  elif [[ -x "${HOME}/.claude/bin/way-embed" ]]; then
    WAY_EMBED_BIN="${HOME}/.claude/bin/way-embed"
  fi

  # Check ways.json for engine override
  local ways_json="${HOME}/.claude/ways.json"
  local forced_engine="auto"
  if [[ -f "$ways_json" ]] && command -v jq >/dev/null 2>&1; then
    forced_engine=$(jq -r '.semantic_engine // "auto"' "$ways_json" 2>/dev/null || echo "auto")
  fi

  _setup_caches() {
    local cache_base="${XDG_CACHE_HOME:-$HOME/.cache}/claude-ways/projects"
    local encoded_project
    encoded_project=$(echo "${PROJECT_DIR:-$PWD}" | tr '/' '-')
    local cache_dir="${cache_base}/${encoded_project}"
    mkdir -p "$cache_dir" 2>/dev/null
    EMBED_CACHE="${cache_dir}/embed-results.tsv"
    BM25_CACHE="${cache_dir}/bm25-results.tsv"
    rm -f "$EMBED_CACHE" "$BM25_CACHE" 2>/dev/null
  }

  _try_embedding() {
    if [[ -n "$WAY_EMBED_BIN" && -x "$WAY_EMBED_BIN" && -f "$MODEL_PATH" && -n "$CORPUS_PATH" ]]; then
      SEMANTIC_ENGINE="embedding"
      _setup_caches
      return 0
    fi
    return 1
  }

  _try_bm25() {
    if [[ -x "$WAYS_BIN" && -n "$CORPUS_PATH" ]]; then
      SEMANTIC_ENGINE="bm25"
      _setup_caches
      return 0
    fi
    return 1
  }

  case "$forced_engine" in
    embedding) _try_embedding || _try_bm25 || SEMANTIC_ENGINE="none" ;;
    bm25)      _try_bm25 || SEMANTIC_ENGINE="none" ;;
    *)         _try_embedding || _try_bm25 || SEMANTIC_ENGINE="none" ;;
  esac
}

# Additive matching: pattern OR semantic (either channel can fire)
# Args: $1=prompt $2=pattern $3=description $4=vocabulary $5=threshold $6=way_id
# Sets: MATCH_CHANNEL ("keyword", "semantic:embedding", or "semantic:bm25") on match
match_way_prompt() {
  local prompt="$1" pattern="$2" description="$3" vocabulary="$4" threshold="$5" way_id="$6"
  MATCH_CHANNEL=""

  # Channel 1: Regex pattern match
  if [[ -n "$pattern" && "$prompt" =~ $pattern ]]; then
    MATCH_CHANNEL="keyword"
    return 0
  fi

  # Channel 2: Semantic match
  # Both engines use batch scoring — one call per prompt, cached for per-way lookups.
  case "$SEMANTIC_ENGINE" in
    embedding)
      # Lazy batch: way-embed scores ALL corpus ways in one call (~20ms).
      if [[ -n "$EMBED_CACHE" && ! -f "$EMBED_CACHE" ]]; then
        local _tmp="${EMBED_CACHE}.tmp"
        if "$WAYS_BIN" embed "$prompt" > "$_tmp" 2>/dev/null; then
          mv "$_tmp" "$EMBED_CACHE"
        else
          rm -f "$_tmp"
        fi
      fi
      if [[ -n "$way_id" && -f "$EMBED_CACHE" ]] && grep -qF "${way_id}	" "$EMBED_CACHE"; then
        MATCH_CHANNEL="semantic:embedding"
        return 0
      fi
      ;;
    bm25)
      # Lazy batch: ways match scores ALL corpus ways in one call (<5ms).
      if [[ -n "$BM25_CACHE" && ! -f "$BM25_CACHE" ]]; then
        local _tmp="${BM25_CACHE}.tmp"
        if "$WAYS_BIN" match "$prompt" > "$_tmp" 2>/dev/null; then
          mv "$_tmp" "$BM25_CACHE"
        else
          rm -f "$_tmp"
        fi
      fi
      if [[ -n "$way_id" && -f "$BM25_CACHE" ]] && grep -qF "${way_id}	" "$BM25_CACHE"; then
        MATCH_CHANNEL="semantic:bm25"
        return 0
      fi
      ;;
  esac

  return 1
}
